# Create a glue crawler

1 - Create a VPC
2 - Create VPC Endpoint - s3 global services
3 - Go to S3 portal create bucket with two folders
	- input
	- output
3 - Go Glue Portal
4 - Select Crawler
5 - Create Crawler - put s3 bucket as source
6 - Create a new IAM role
7 - Run Crawler
8 - Review execution and database (go to logs)
